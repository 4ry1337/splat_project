package splat.lexer;

public enum TokenType {
  PROGRAM, BEGIN, END,

  LEFT_PAREN, RIGHT_PAREN,
  COMMA, DOT, COLON, SEMICOLON,

  PLUS, MINUS, SLASH, STAR, MOD,

  AND, OR, NOT,
  ASSIGNMENT,
  EQUAL,
  GREATER, GREATER_EQUAL,
  LESS, LESS_EQUAL,

  LABEL,

  // literal
  STRING,
  INTEGER,
  BOOLEAN,
  TRUE, FALSE,
  VOID,

  IS,
  WHILE, DO,
  IF, THEN, ELSE,
  PRINT, PRINT_LINE,
  RETURN,
}
